package br.senac.pi;

public class Operações {

	public static void main(String[] args) {
		
		String nome = "Francisco";
		String sobrenome = "André";
		
		System.out.println("Nome completo: " + nome + sobrenome);
		
		System.out.println("-----------------------------------");
		
		int idade = 18;
		boolean brasileiro = true;
		
		System.out.println("Idade: " + idade);
		System.out.println("Brasileiro: " + brasileiro);
		
		System.out.println("Operações matematicas");
		int n1 = 10;
		int n2 = 5;
		System.out.println("O valor de n1 é: " + n1 );
		System.out.println("O valor de n2 é: " + n2);
		System.out.println("A soma entre n1 e n2 é: " + (n1 + n2));
		System.out.println("A multiplicação entre n1 e n2 é: " + n1 * n2);
		System.out.println("A subitração entre n1 e n2 é: " + (n1 - n2));
		System.out.println("A divisão entre n1 e n2 é: " + n1 / n2);
		System.out.println(" O resto da divisão entre n1 e n2 é: " + n1 % n2);
		
		

	}

}
